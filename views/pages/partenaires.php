<h2>Pourquoi nous soutenir ?</h2>

	<img class="vignette" style="float: right;" src="_/images/pages/partenaires_1.jpg" alt="médiatisation du raid">
	<p>
		Nous sponsoriser est un moyen <strong>bon marché</strong> de
		réaliser une publicité pour votre entreprise.<br>
		Notre 4L accroche les regards et représente une support de communication 
		original et itinérant qui sera vu par des milliers de personnes.
	</p>

	<p>
		<strong>Votre image est associée à un projet humanitaire important</strong>. 
		Vous soutenez comme valeurs le partage, la solidarité et le développement durable.<br>
		Ce raid permet un <strong>soutien concret aux villages marocains</strong>,
		empêcher leur désertification et favoriser leur
		développement grâce au travail mené par Coeur de
		Gazelles en accord avec le gouvernement marocain.
	</p>

	<p>
		<strong>Votre image sera relayée dans toute la France</strong>, en Espagne et Maroc durant 2014 et 2015.<br>
		Et nous participerons à toute manifestation ou
		exposition permettant de mettre en valeur votre
		entreprise.<br>
		Nous avons déjà <strong>figuré en bonne place dans
		le Ouest-France</strong> et au Journal de Vitré. Ce sont
		des articles où nous <strong>parlerons de vous</strong> que
		vous pourrez montrer à vos clients.
	</p>


