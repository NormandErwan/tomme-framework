<h1>Le raid Students Challenge</h1>

<h2>Une aventure sportive et solidaire</h2>

	<img class="vignette" style="float: right;" src="_/images/pages/students_challenge_1.jpg" title="Une aventure sportive et solidaire" alt="Une aventure sportive et solidaire" />

	<p>
		Le Students Challenge est un raid étudiant solidaire. C'est une épreuve automobile basée sur <strong>l'orientation et la régularité</strong>.
	</p>

	<p>
		Cependant, la vitesse n’est jamais recherchée : l’objectif est de savoir s’orienter à la lecture des road-books remis par 
		l’organisation et de savoir prendre des caps afin de <strong>minimiser le nombre de kilomètres à parcourir</strong>.<br>
		Les pièges d’oueds et de sable sont nombreux et l’entraide primordiale ! C’est l’occasion d’apprendre à avancer quel que soit l’obstacle.
	</p>
	
	<p>
		Cette 8e édition se déroulera durant <strong>10 jours en février 2015</strong> et consiste en un <strong>parcours de 7000 km</strong> à travers les pistes dans 
		le désert marocain. Il est organisé par la société Maïenga en partenariat avec l'assocation Coeur de Gazelles qui préparent 
		également chaque année le rallye Aïcha des Gazelles du Maroc.
	</p>

<h2>La Green Day</h2>

	<img class="vignette" style="float: left;" src="_/images/pages/students_challenge_2.jpg" title="La Green Day" alt="La Green Day" />
	<p>
		Le moment fort du Students Challenge est la Green Day : journée <strong>consacrée à la plantation de palmiers-dattiers</strong> par les 
		participants dans un village du sud du Maroc.
	</p>

	<p>
		Cette initiative, <strong>reconnue par le Programme des Nations Unies pour l'Environnement</strong> et partie intégrante du projet « Plantons pour la Planète », 
		est un projet concret d'aide à un village marocain.<br>
		Ce projet permet d'une part de <strong>lutter contre les problèmes de désertification</strong> et de changement climatiques, d'une autre part permet aux habitant 
		du villagedes revenus supplémentaires par la vente des dattes.
	</p>
	
	<p>
		Le projet est financé par tous les équipages et organisé par Coeur de Gazelle qui procédera à l'achat des palmier et à la mise en place d'actions
		complémentaires pour le village.
	</p>
