<h2>L'association SAND 4L</h2>

	<img style="float: right; height: 60px;" src="_/images/pages/logo_complet.png" title="Logo de SAND 4L" alt="Logo SAND 4L" />
	
	<p>
		Solidarité et Aventure Nomade dans le Désert en 4L
		( SAND 4L ) est une association de loi 1901 à caractère
		humanitaire, que nous avons fondée en juillet 2012. <br />
		L’objectif : contribuer aux
		projets d'aide aux populations du Maroc en participant à des raids humanitaires
		et solidaires.
	</p>

	<p>
		À la tête du projet et de l’association : Florent Guiotte et Erwan Normand.
	</p>

